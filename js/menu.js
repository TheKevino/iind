//cambiar color del texto del boton residencias
$('#residencias').hover(function(){
    $('#residencias .contenido-boton i, #residencias .contenido-boton p').css({
      "color": "#9e9e9e"
    });
});

$('#residencias').mouseover(function(){
    $('#residencias .contenido-boton i, #residencias .contenido-boton p').css({
      "color": "white"
    });
});

